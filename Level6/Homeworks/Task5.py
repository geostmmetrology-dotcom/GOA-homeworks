"""კომეტარებით ახსენით თუ რა განსხვავებაა /, //, და % მათემატიკურ ოპერატორებს შორის, დაწერეთ რას აკეთებს თითოეული"""
# /  — ჩვეულებრივი გაყოფა
# შედეგი ყოველთვის ათწილადი (float) იქნება, თუნდაც ზუსტად იყოფოდეს
print(7 / 2)   # შედეგი: 3.5
print(10 / 5)  # შედეგი: 2.0

# // — მთლიანი გაყოფა (integer)
# შედეგი იქნება მხოლოდ მთელი ნაწილი, ათწილადი მოიჭრება
print(7 // 2)  # შედეგი: 3
print(10 // 5) # შედეგი: 2
print(9 // 4)  # შედეგი: 2 (ნაშთი იგნორირებულია)

# % — გაყოფის ნაშთი (modulus)
# აბრუნებს მხოლოდ ნაშთს, ანუ რამდენი დარჩა გაყოფის შემდეგ
print(7 % 2)   # შედეგი: 1  (რადგან 7 = 2*3 + 1)
print(10 % 5)  # შედეგი: 0  (რადგან ზუსტად იყოფა)
print(9 % 4)   # შედეგი: 1  (რადგან 9 = 4*2 + 1)
