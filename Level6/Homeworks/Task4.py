"""4) თითოეულ მათემატიკურ ოპერატორზე (+, -, *, /, %, **, //) შეასრულეთ 5 მაგალითი და დაბეჭდეთ შედეგი"""
# დამატება (+)
print(5 + 3)
print(10 + 7)
print(2 + 9)
print(100 + 250)
print(1 + 0)
print()

# გამოკლება (-)
print(10 - 3)
print(50 - 25)
print(9 - 2)
print(100 - 99)
print(0 - 5)
print()

# გამრავლება (*)
print(4 * 3)
print(7 * 2)
print(10 * 10)
print(5 * 0)
print(12 * 11)
print()

# გაყოფა (/)
print(10 / 2)
print(9 / 3)
print(7 / 2)
print(5 / 4)
print(100 / 25)
print()

# ნაშთი (%) — modulus
print(10 % 3)
print(9 % 2)
print(15 % 4)
print(7 % 5)
print(100 % 6)
print()

# ახარისხება (**)
print(2 ** 3)
print(3 ** 2)
print(5 ** 4)
print(10 ** 0)
print(7 ** 1)
print()

# მთლიანი გაყოფა (//)
print(10 // 3)
print(9 // 2)
print(15 // 4)
print(20 // 5)
print(100 // 6)